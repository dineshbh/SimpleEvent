<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">O campo das representa&ccedil;&otilde;es sociais se inaugura com a pesquisa de Serge Moscovici sobre <span style="mso-spacerun: yes;">&nbsp;</span>a representa&ccedil;&atilde;o social da Psican&aacute;lise, publicada na Fran&ccedil;a em 1961. O campo, portanto, se constitui primeiramente na Europa.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;">&nbsp;</p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">Em 1982, Denise Jodelet visita o Brasil, iniciando na UFPB, em Campina Grande, a difus&atilde;o da teoria das RS e uma colabora&ccedil;&atilde;o com pesquisadores brasileiros que iriam se expandir por todo o pa&iacute;s at&eacute; os dias de hoje.<span style="mso-spacerun: yes;">&nbsp; </span></span></p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; margin-bottom: .0001pt; text-align: justify; line-height: normal;">&nbsp;</p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">A 1a Confer&ecirc;ncia Internacional sobre RS em Ravello, It&aacute;lia, em 1992, abre uma sequ&ecirc;ncia de eventos que re&uacute;nem os pesquisadores da &aacute;rea provenientes de v&aacute;rios pa&iacute;ses.<span style="mso-spacerun: yes;">&nbsp; </span>As Conferencias Internacionais v&atilde;o pontuar e alimentar o desenvolvimento do campo internacionalmente. Acontecem de dois em dois anos, cada vez em um continente. A segunda Confer&ecirc;ncia Internacional sobre RS ocorre no Rio de Janeiro em 1994, trazendo um significativo avan&ccedil;o para o campo no Brasil. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;">&nbsp;</p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">Depois deste importante momento e do Encontro de pesquisadores brasileiros e estrangeiros em Jo&atilde;o Pessoa em 1996, realiza-se a primeira Jornada Internacional sobre RS em Natal, em 1998, que se repetir&aacute; desde ent&atilde;o. A sequ&ecirc;ncia deste tipo de evento tem se confirmado a cada dois anos, a partir de 2001, cada vez em uma localidade diferente, tornando-se as Jornadas Internacionais e Confer&ecirc;ncia Brasileira em RS. </span></p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;">&nbsp;</p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">As JIRS/CBRS s&atilde;o eventos internacionais/ nacionais itinerantes, transdisciplinares,<span style="mso-spacerun: yes;">&nbsp; </span>nos quais se re&uacute;nem pesquisadores e professores participantes <span style="mso-spacerun: yes;">&nbsp;</span>de diversos pa&iacute;ses. T&ecirc;m um car&aacute;ter pedag&oacute;gico ao colocar em contato estudiosos de n&iacute;veis diversos de aproxima&ccedil;&atilde;o e experi&ecirc;ncia com a teoria das representa&ccedil;&otilde;es sociais, permitindo o debate dos trabalhos em grupos de discuss&atilde;o e sess&otilde;es interativas de posters, destinados a uma troca direta entre pesquisadores iniciantes e outros, assim como entre &aacute;reas de conhecimento diferentes.<span style="mso-spacerun: yes;">&nbsp; </span></span></p>
<p class="MsoNormal" style="mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">Em 2015, a IX JIRS e VII Confer&ecirc;ncia Brasileira sobre Representa&ccedil;&otilde;es Sociais ter&atilde;o lugar em Teresina, Piau&iacute;, em novembro, quando se completar&aacute; um ano do falecimento de Serge Moscovici. Ser&aacute; mais uma ocasi&atilde;o de homenagear o fundador da teoria, celebrando sua obra. </span></p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">As IX Jornadas Internacionais e VII Confer&ecirc;ncia Brasileira sobre Representa&ccedil;&otilde;es Sociais constituem um momento na trajet&oacute;ria de Confer&ecirc;ncias e Jornadas Nacionais e Internacionais sobre Representa&ccedil;&otilde;es Sociais. </span></p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">O tema do evento, </span><strong><span style="font-family: 'Calibri','sans-serif'; color: black; mso-themecolor: text1; font-weight: normal; mso-bidi-font-weight: bold;">Representa&ccedil;&otilde;es Sociais e Sociedades em Movimento, traz para o campo das representa&ccedil;&otilde;es sociais a atualidade pol&iacute;tico-social brasileira e de tantos outros pa&iacute;ses nos anos 2010, que tem sido alvo de intenso debate nas ci&ecirc;ncias sociais. As representa&ccedil;&otilde;es sociais, como fen&ocirc;meno que se produz ao contato com a novidade, fazem parte deste universo, na comunica&ccedil;&atilde;o midi&aacute;tica, nas redes sociais e em tantas outras formas de intera&ccedil;&atilde;o social. Elas est&atilde;o na subst&acirc;ncia do pensamento social, cuja din&acirc;mica se acelera em momentos de efervesc&ecirc;ncia pol&iacute;tica, social e cultural. &Eacute; neste quadro de mudan&ccedil;as sociais que se pretende prosseguir no avan&ccedil;o te&oacute;rico-metodol&oacute;gico das diversas linhas de trabalho com a teoria das representa&ccedil;&otilde;es sociais, abrindo espa&ccedil;o para a continuidade da discuss&atilde;o conceitual com outras perspectivas te&oacute;ricas e a reflex&atilde;o sobre a postura cr&iacute;tica e o envolvimento com os fatos de sociedade. </span></strong></p>
<p class="MsoNormal" style="mso-margin-top-alt: auto; mso-margin-bottom-alt: auto; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; color: black; mso-themecolor: text1; mso-fareast-language: PT-BR;">Este tema nos aproxima da preocupa&ccedil;&atilde;o de Moscovici diante das grandes quest&otilde;es da atualidade, relembrando seu compromisso com o movimento social, no terreno da ecologia, e sua valiosa contribui&ccedil;&atilde;o para os fundamentos do pensamento ecol&oacute;gico europeu.<span style="mso-spacerun: yes;">&nbsp; </span></span></p>
