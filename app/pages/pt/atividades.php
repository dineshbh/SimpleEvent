<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><strong><span style="mso-fareast-font-family: 'Times New Roman'; mso-bidi-font-family: 'Times New Roman'; mso-fareast-language: PT-BR;">Simp&oacute;sios(SIMP)</span></strong></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="mso-fareast-font-family: 'Times New Roman'; mso-bidi-font-family: 'Times New Roman'; mso-fareast-language: PT-BR;"><br /> Os simp&oacute;sios, s&atilde;o compostos por tr&ecirc;s ou quatro pesquisadores/as que v&ecirc;m se dedicando ao aprofundamento de quest&otilde;es te&oacute;rico-metodol&oacute;gicas, oriundas da teoria das representa&ccedil;&otilde;es sociais, essenciais ao enfrentamento dos desafios da contemporaneidade. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;">&nbsp;</p>
<p class="MsoNormal" style="text-align: justify;"><strong><span style="mso-fareast-font-family: 'Times New Roman'; mso-bidi-font-family: 'Times New Roman'; mso-fareast-language: PT-BR;">Mesas-redondas(MR)</span></strong></p>
<p class="MsoNormal" style="text-align: justify;"><span style="mso-fareast-font-family: 'Times New Roman'; mso-bidi-font-family: 'Times New Roman'; mso-fareast-language: PT-BR;"><br /> As mesas-redondas t&ecirc;m por prop&oacute;sito discutir temas de interesse geral e de especial relev&acirc;ncia no debate te&oacute;rico-metodol&oacute;gico contempor&acirc;neo no campo dos estudos das representa&ccedil;&otilde;es sociais. Cada mesa contar&aacute; com a participa&ccedil;&atilde;o de tr&ecirc;s a quatro expositores/as, preferencialmente de duas institui&ccedil;&otilde;es diferentes. O/A proponente da mesa-redonda, bem como seus membros, devem estar inscritos/as na </span><strong><span style="color: black;">IX JIRS e VII CBRS</span></strong><span style="mso-fareast-font-family: 'Times New Roman'; mso-bidi-font-family: 'Times New Roman'; mso-fareast-language: PT-BR;">. Cada apresentador/a ter&aacute; entre 20 e 30 minutos para exposi&ccedil;&atilde;o. A divulga&ccedil;&atilde;o de conhecimentos derivados de uma pesquisa ou atividade profissional isolada n&atilde;o se enquadra nesta modalidade. O m&eacute;rito da proposta ser&aacute; analisado pela Comiss&atilde;o Cient&iacute;fica.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;">A Comiss&atilde;o cient&iacute;fica propor&aacute; a organiza&ccedil;&atilde;o de alguns simp&oacute;sios. Grupos de pesquisadores, redes etc. tamb&eacute;m poder&atilde;o propor simp&oacute;sios ou mesas redondas.</p>
<p><strong>Grupos de Discuss&atilde;o Tem&aacute;tico - GDT</strong></p>
<p>Os GDT s&atilde;o espa&ccedil;os para a apresenta&ccedil;&atilde;o de relatos de pesquisa sobre uma mesma tem&aacute;tica, desenvolvidos por grupos ou pesquisadores distintos, provenientes de v&aacute;rias institui&ccedil;&otilde;es. A apresenta&ccedil;&atilde;o oral oportuniza a interlocu&ccedil;&atilde;o com outros pesquisadores da mesma sess&atilde;o e com a plateia. Os GDT ser&atilde;o coordenados por um especialista, a convite da comiss&atilde;o cient&iacute;fica do evento. Ficar&aacute; sob a responsabilidade do coordenador do GDT as sequ&ecirc;ncias das apresenta&ccedil;&otilde;es dos trabalhos, bem como a coordena&ccedil;&atilde;o do debate e a mobiliza&ccedil;&atilde;o para a participa&ccedil;&atilde;o da audi&ecirc;ncia. A dura&ccedil;&atilde;o das sess&otilde;es ser&aacute; de 2 horas, com a participa&ccedil;&atilde;o de at&eacute; 10 expositores em cada.</p>
<p><strong>Compete &agrave; comiss&atilde;o cient&iacute;fica do evento a composi&ccedil;&atilde;o dos GDT</strong>.</p>
